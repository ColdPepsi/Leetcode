public class Solution {
    public int nthUglyNumber(int n) {
        int[] dp = new int[n];
        dp[0] = 1;
        int a = 0, b = 0, c = 0;
        for (int i = 1; i < n; i++) {
            int n2 = dp[a] * 2, n3 = dp[b] * 3, n5 = dp[c] * 5;
            dp[i] = getMin(n2, n3, n5);
            if (dp[i] == n2) {
                a++;
            }
            if (dp[i] == n3) {
                b++;
            }
            if (dp[i] == n5) {
                c++;
            }
        }
        return dp[n - 1];
    }

    /**
     * 返回三个数最小和
     *
     * @param a
     * @param b
     * @param c
     * @return
     */
    private int getMin(int a, int b, int c) {
        return Math.min(a, Math.min(b, c));
    }
}