/**
 * 类Slution2.java的实现描述：m和n找公共前缀，后面后面补0即可
 *
 * @author wubiao21 2020年08月24日 13:42:40
 */
public class Slution2 {
    public int rangeBitwiseAnd(int m, int n) {
        int offset = 0;
        while (m != n) {
            m >>= 1;
            n >>= 1;
            offset++;
        }
        return m << offset;
    }
}