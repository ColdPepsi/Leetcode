/**
 * 此解法为正规的动态规划
 * 但是没有解决溢出
 *
 * @author wubiao21 2020年07月17日 13:42:42
 */
public class Solution {


    public int cuttingRope(int n) {
        int[] dp = new int[n + 1];
        dp[1] = 1;
        dp[2] = 2;
        dp[3] = 3;
        for (int i = 4; i <= n; i++) {
            for (int j = 1; j < i; j++) {
                dp[i] = Math.max(dp[i], Math.max(j * (i - j), j * dp[i - j]));
            }
        }
        return dp[n];
    }
}